    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String fecha = txtFecha.getText().trim(); // Obtener la c√©dula ingresada

        if (fecha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese una fecha", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Llamar al controlador para buscar el due√±o
        CitasMedicas citas = citasMedicasControlador.buscarCitas(fecha);
        
        DefaultTableModel modelo = (DefaultTableModel) tablaCitas.getModel();

        if (citas != null) {
            
            modelo.addRow(new Object[]{
            citas.getId(),
            citas.getFecha(),
            citas.getHora(),
            citas.getMascotaId(),
            citas.getDuenoId(),
            citas.getEstado()
        });
           
        } else {
            JOptionPane.showMessageDialog(this, "Due√±o no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:

       //int id = Integer.parseInt(tx.getText().trim());
        String fecha = txtFecha.getText().trim();
        String hora = txtHora.getText().trim();
        int mascotaId = Integer.valueOf(txtMascota.getText().trim());
        int duenoId = Integer.valueOf(txtDueno.getText().trim());
        String estado = (String) comboEstado.getSelectedItem();

        //String cedula = txtCedula.getText().trim();

        // Verificar que los campos no est√©n vac√≠os
        if (fecha.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "El Nombre es obligatorio.");
            return;}

        if  (hora.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "La Cedula es obligatoria.");

            return;}

        if (estado.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "El Telefono es obligatorio.");
            return;}

        // Crear un objeto Due√±o con los nuevos datos
        CitasMedicas citas = new CitasMedicas(fecha, hora, mascotaId, duenoId,estado);

        // Llamar al controlador para actualizar
        boolean actualizado = citasMedicasControlador.actualizarCitas(citas);

        if (actualizado) {
            JOptionPane.showMessageDialog(this, "Due√±o actualizado correctamente", "√âxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

