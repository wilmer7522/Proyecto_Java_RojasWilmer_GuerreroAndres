    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:

        // Capturar datos del formulario
        String fecha = txtFecha.getText();
        String hora = txtHora.getText();
        int mascotaId = Integer.valueOf(txtMascota.getText());
        int duenoId = Integer.valueOf(txtDueno.getText());
        String estado = (String) comboEstado.getSelectedItem();
        

        // Verificar que los campos no est√©n vac√≠os
        if (fecha.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "La fecha es obligatoria.");
            return;}

        if  (hora.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "La Hora es obligatoria.");

            return;}

        if (estado.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "El Estado es obligatorio.");
            return;}

        // Crear objeto due√±o
        CitasMedicas nuevaCita = new CitasMedicas(fecha, hora, mascotaId, duenoId, estado);

        // Llamar al controlador para agregarlo
        CitasMedicasControlador controlador = new CitasMedicasControlador(citasMedicasDAO);
        boolean agregado = controlador.agregarCitas(nuevaCita);

        if (agregado) {
            javax.swing.JOptionPane.showMessageDialog(this, "Due√±o agregado exitosamente.");
            limpiarCampos();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error al agregar el due√±o.");
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

