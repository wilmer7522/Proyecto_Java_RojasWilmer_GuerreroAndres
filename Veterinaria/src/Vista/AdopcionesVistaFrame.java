    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        DuenoControlador controlador = new DuenoControlador(duenoDAO);
    controlador.cargarDuenosEnTabla(tablaDatosDueno);



// TODO add your handling code here:
        int id = Integer.parseInt(txtId.getText().trim());

        if (String.valueOf(id).isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un ID", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Llamar al controlador para buscar el due√±o
        Mascota mascota = mascotaControlador.buscarMascota(id);

        if (mascota != null) {
            // Mostrar los datos en los campos de texto
            txtId.setText(String.valueOf(mascota.getId()));

            txtNombre.setText(mascota.getNombre());
            txtEspecie.setText(mascota.getEspecie());
            txtRaza.setText(mascota.getRaza());
            txtEdad.setText(String.valueOf(mascota.getEdad()));
            txtFechaNacimiento.setText(mascota.getFechaNacimiento());
            txtSexo.setText(mascota.getSexo());
            txtMicrochip.setText(mascota.getMicrochipTatuaje());
            txtFoto.setText(mascota.getFoto());
            txtDuenoId.setText(String.valueOf(mascota.getDueno()));
        } else {
            JOptionPane.showMessageDialog(this, "Mascota no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Obtener la c√©dula ingresada en el JTextField
        int duenoId = Integer.parseInt(txtDuenoId.getText().trim());

        // Validar que el usuario haya ingresado algo
        /* if (cedula.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese una c√©dula", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }*/

        // Buscar el due√±o en la base de datos
        Dueno dueno = duenoControlador.buscarDuenoId(duenoId);

        // Obtener el modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel) tablaDatosDueno.getModel();

        // Limpiar la tabla antes de agregar el nuevo resultado
        modelo.setRowCount(0);

        if (dueno != null) {
            // Agregar los datos del due√±o a la tabla
            modelo.addRow(new Object[]{
                dueno.getId(),
                dueno.getCedula(),
                dueno.getNombre(),
                dueno.getDireccion(),
                dueno.getTelefono(),
                dueno.getCorreo_electronico(),
                dueno.getContacto_emergencia()
            });
        } else {
            // Mostrar mensaje si no se encontr√≥ el due√±o
            JOptionPane.showMessageDialog(this, "No se encontr√≥ un due√±o con esa c√©dula", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:

        int id = Integer.parseInt(txtId.getText().trim());
        String nombre = txtNombre.getText().trim();
        String especie = txtEspecie.getText().trim();
        String raza = txtRaza.getText().trim();
        int edad = Integer.parseInt(txtEdad.getText().trim());
        String fechaNac = txtFechaNacimiento.getText().trim();

        String sexo = txtSexo.getText().trim();
        String microchip = txtMicrochip.getText().trim();
        String foto = txtFoto.getText().trim();

        String duenoIdTexto = txtDuenoId.getText().trim();
        Integer duenoId = null;

        if (!duenoIdTexto.isEmpty()) {
            try {
                duenoId = Integer.parseInt(duenoIdTexto); // Convierte solo si no est√° vac√≠o
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El ID del due√±o debe ser un n√∫mero v√°lido.", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Evita continuar con la actualizaci√≥n si hay un error
            }
        }
        //Integer dueniId = Integer.valueOf(txtDuenoId.getText().trim());

        // Verificar que los campos no est√©n vac√≠os
        if (nombre.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "El Nombre es obligatorio.");
            return;}

        /* if  (cedula.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "La Cedula es obligatoria.");

            return;}

        if (telefono.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "El Telefono es obligatorio.");
            return;}*/

        // Crear un objeto Due√±o con los nuevos datos
        Mascota mascota = new Mascota(id,nombre, especie, raza, edad,fechaNac,sexo,microchip,foto,duenoId);

        // Llamar al controlador para actualizar
        boolean actualizado = mascotaControlador.actualizarMascota(mascota);

        if (actualizado) {
            JOptionPane.showMessageDialog(this, "Mascota actualizada correctamente", "√âxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Error al actualizar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

